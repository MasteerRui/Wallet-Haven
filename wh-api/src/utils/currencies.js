export const getSupportedCurrencyCodes = () => {
  return SUPPORTED_CURRENCIES.map(currency => currency.code);
};

export const isCurrencySupported = (currencyCode) => {
  if (!currencyCode || typeof currencyCode !== 'string') {
    return false;
  }
  const codes = getSupportedCurrencyCodes();
  return codes.includes(currencyCode.toUpperCase());
};

export const getCurrencyInfo = (currencyCode) => {
  if (!currencyCode) return null;
  return SUPPORTED_CURRENCIES.find(
    currency => currency.code.toUpperCase() === currencyCode.toUpperCase()
  ) || null;
};

